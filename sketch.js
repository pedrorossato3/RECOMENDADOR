function atualizarRecomendacao() {
  let generoMasculino = campoGeneroMasculino.checked();
  let generoFeminino = campoGeneroFeminino.checked();
  if (!generoMasculino && !generoFeminino) {
    mostrarMensagem("Por favor, selecione seu g√™nero.");
    return;
  }
  let gostaDeNacional = campoGostaNacional.checked();
  let gostaDeArabe = campoGostaArabe.checked();
  let novasOpcoes = [];
  if (generoMasculino) {
    if (gostaDeNacional) novasOpcoes = novasOpcoes.concat(perfumesNacionaisMasculinos);
    if (gostaDeArabe) novasOpcoes = novasOpcoes.concat(perfumesArabesMasculinos);
  } else if (generoFeminino) {
    if (gostaDeNacional) novasOpcoes = novasOpcoes.concat(perfumesNacionaisFemininos);
    if (gostaDeArabe) novasOpcoes = novasOpcoes.concat(perfumesArabesFemininos);
  }
  if (botaoDia.checked()) {
    novasOpcoes = novasOpcoes.filter(p => perfumesDia.includes(p));
  } else if (botaoNoite.checked()) {
    novasOpcoes = novasOpcoes.filter(p => perfumesNoite.includes(p));
  }
  if (novasOpcoes.length === 0) {
    mostrarMensagem("Experimente novas fragr√¢ncias! Marque pelo menos uma prefer√™ncia.");
    opcoesAtuais = [];
    indiceAtual = -1;
    return;
  }
  if (!arraysIguais(novasOpcoes, opcoesAtuais)) {
    opcoesAtuais = novasOpcoes;
    indiceAtual = -1;
  }
  indiceAtual = (indiceAtual + 1) % opcoesAtuais.length;

  mostrarPerfume(opcoesAtuais[indiceAtual]);
}
