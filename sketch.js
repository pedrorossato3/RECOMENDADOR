let opcoesAtuais = [];
let indiceAtual = -1;

function atualizarRecomendacao() {
  let generoMasculino = campoGeneroMasculino.checked();
  let generoFeminino = campoGeneroFeminino.checked();
  if (!generoMasculino && !generoFeminino) {
    mostrarMensagem("Por favor, selecione seu gênero.");
    return;
  }
  let gostaDeNacional = campoGostaNacional.checked();
  let gostaDeArabe = campoGostaArabe.checked();
  let novasOpcoes = [];
  if (generoMasculino) {
    if (gostaDeNacional) novasOpcoes = novasOpcoes.concat(perfumesNacionaisMasculinos);
    if (gostaDeArabe) novasOpcoes = novasOpcoes.concat(perfumesArabesMasculinos);
  } else if (generoFeminino) {
    if (gostaDeNacional) novasOpcoes = novasOpcoes.concat(perfumesNacionaisFemininos);
    if (gostaDeArabe) novasOpcoes = novasOpcoes.concat(perfumesArabesFemininos);
  }
  if (botaoDia.checked()) {
    novasOpcoes = novasOpcoes.filter(p => perfumesDia.includes(p));
  } else if (botaoNoite.checked()) {
    novasOpcoes = novasOpcoes.filter(p => perfumesNoite.includes(p));
  }
  if (novasOpcoes.length === 0) {
    mostrarMensagem("Experimente novas fragrâncias! Marque pelo menos uma preferência.");
    opcoesAtuais = [];
    indiceAtual = -1;
    return;
  }
  if (!arraysIguais(novasOpcoes, opcoesAtuais)) {
    opcoesAtuais = novasOpcoes;
    indiceAtual = -1;
  }
  indiceAtual = (indiceAtual + 1) % opcoesAtuais.length;

  mostrarPerfume(opcoesAtuais[indiceAtual]);
}
function mostrarMensagem(msg) {
  textoRecomendacao.html(msg);
  videoContainer.html('');
  descricaoContainer.html('');
}
function arraysIguais(a, b) {
  if (a.length !== b.length) return false;
  for (let i = 0; i < a.length; i++) {
    if (a[i] !== b[i]) return false;
  }
  return true;
}
function mostrarPerfume(nomePerfume) {
  textoRecomendacao.html(`<strong>${nomePerfume}</strong>`);
  let dados = dadosPerfumes[nomePerfume];
  if (!dados) {
    videoContainer.html('');
    descricaoContainer.html('');
    return;
  }
  // Monta a imagem
  let htmlImagem = '';
  if (dados.imagem) {
    htmlImagem = `<img src="${dados.imagem}" alt="${nomePerfume}" style="max-width: 360px; border-radius: 10px; box-shadow: 0 0 10px rgba(255,255,255,0.2);">`;
  }
  // Exibe imagem e vídeo juntos
  videoContainer.html(htmlImagem);
  descricaoContainer.html(dados.descricao);
}
